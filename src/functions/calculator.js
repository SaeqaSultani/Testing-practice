 class Calculator{
    constructor(){}

    add(num1,num2){
        const sum = num1 + num2;
        return sum;
    }

    subtract(num1,num2){
        const subt = num1 - num2;
        return subt;
    }

    multiply(num1,num2){
        const mult = num1 * num2;
        return mult;
    }

    divide(num1,num2){
        const divi = num1 / num2;
        return divi;
    }
  
}

module.exports = Calculator;






